#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Simple Calculations
    printf("%d + %d = %d\n", 5, 5, 5+5);
    printf("%.1f - %.1f = %.1f\n", 15.5, 9.5, 15.5-9.5);
    printf("%d * %d = %d\n", 5, 5, 5*5);
    printf("%d / %d = %d\n", 590, 5, 590/5);
    printf("%d %% %d = %d\n", 62, 5, 62%5);
    return 0;
}
